create table bookings (
       id int8 generated by default as identity,
        description varchar(4000),
        end_time time,
        start_time time,
        subject varchar(255),
        candidate_slot_id int8,
        interviewer_slot_id int8,
        primary key (id)
    );

create table candidate_slots (
       id int8 generated by default as identity,
        date date,
        candidate_email varchar(255),
        end_time time,
        start_time time,
        primary key (id)
    );

create table interviewer_slots (
       id int8 generated by default as identity,
        day_of_week varchar(255),
        end_time time,
        start_time time,
        week int4 not null,
        interviewer_id int8,
        primary key (id)
    );

create table users (
        id int8 generated by default as identity,
        email varchar(255),
        current_week int4,
        next_week int4,
        role varchar(255),
        primary key (id)
    );

